package Basics;

public class IfElse {
    public static void main(String[] args) {
        int day = 3;

        if (day == 1) {
            System.out.println("Go to the Home");
        } else if (day == 2) {
            System.out.println("Go to the Village");
        } else if (day == 3) {
            System.out.println("Go to the Gym");
        } else {
            System.out.println("You Can Go Anywhere!");
        }
    }
}
